import 'package:academia/core/core.dart';
import 'package:academia/features/features.dart';
import 'package:dartz/dartz.dart';

class CachePostRepliesUsecase {
  final ChirpRepository chirpRepository;

  CachePostRepliesUsecase({required this.chirpRepository});


  Future<Either<Failure, List<PostReply>>> call(String postId) async {
    return chirpRepository.cachePostReplies(postId);
  }
}
